# 1. *Лічильник символів:* Напишіть програму, яка приймає рядок від користувача та підраховує кількість кожного символу у рядку.
# Виведіть результат у вигляді словника, де ключами будуть символи, а значеннями - їх кількість в рядку.

# Запит користувача на введення рядка
input_string = input("Введіть рядок: ")

# Створення порожнього словника для підрахунку символів
char_count = {}

# Підрахунок кількості кожного символу у рядку
for char in input_string:
    if char in char_count:
        char_count[char] += 1
    else:
        char_count[char] = 1

# Виведення результату у вигляді словника
print("Лічильник символів:")
print(char_count)
# 2. *Перевірка дублікатів:* Створіть функцію, яка приймає список і повертає True,
# якщо у списку є дублікати, і False в іншому випадку. Використайте словник для відстеження кількості зустрічень кожного елемента у списку.

# Функція для перевірки дублікатів у списку
def check_duplicates(input_list):
    # Створення порожнього словника для відстеження кількості зустрічей кожного елемента
    count_dict = {}
    for item in input_list:
        if item in count_dict:
            return True
        else:
            count_dict[item] = 1
    return False

# Приклад використання функції
my_list = [1, 2, 3, 4, 5, 1]
print("Дублікати у списку:", check_duplicates(my_list))

# 3. *Об'єднання словників:* Напишіть програму, яка об'єднує два словники в один. Якщо ключі повторюються, використовуйте значення з другого словника.

# Словники для об'єднання
dict1 = {'a': 1, 'b': 2, 'c': 3}
dict2 = {'b': 4, 'd': 5, 'e': 6}

# Об'єднання словників
merged_dict = dict1.copy()
merged_dict.update(dict2)

# Виведення об'єднаного словника
print("Об'єднаний словник:")
print(merged_dict)
